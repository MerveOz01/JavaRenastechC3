package day02_variables;

public class c1_printvsprintln {

    public static void main( String[] args){
        //first Line
        System.out.println("This is println");

        // second line
        System.out.print("--this is print"); // println will break current line
        // it will continue same line that you are using

        //second line
        System.out.print("-- continue to print"); // print wont break the line

        //second Line
        System.out.print("---continue print 2"); // print wont break line

        // second line
        System.out.println("println2"); // first it will print this in second line
        // since we used println now next line will be third

        // third line
        System.out.print("print 3"); // this won't break the line

        // third line
        System.out.println("println3");

        //fourth line
        System.out.println("println4");









    }
}
